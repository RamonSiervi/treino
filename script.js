function createExerciseCard(ex) {
  const card = document.createElement('div');
  card.className = 'card';

  const info = document.createElement('div');
  info.className = 'info';

  const h3 = document.createElement('h3');
  h3.textContent = ex.name;
  info.appendChild(h3);

  const meta = document.createElement('div');
  meta.className = 'meta';

  const s = document.createElement('span');
  s.className = 'tag'; s.innerHTML = '<strong>S√©ries:</strong> ' + ex.series;
  const c = document.createElement('span');
  c.className = 'tag'; c.innerHTML = '<strong>Carga:</strong> ' + ex.load;
  if (ex.rest && ex.rest !== '-') {
    const r = document.createElement('span');
    r.className = 'tag'; r.innerHTML = '<strong>Intervalo:</strong> ' + ex.rest;
    meta.appendChild(r);
  }
  meta.prepend(c); meta.prepend(s);
  info.appendChild(meta);

  const links = document.createElement('div');
  links.className = 'links';
  if (ex.demo) {
    const a = document.createElement('a');
    a.href = ex.demo; a.target = '_blank'; a.rel = 'noopener';
    a.textContent = 'Abrir v√≠deo de exemplo';
    links.appendChild(a);
  }
  info.appendChild(links);

  const thumb = document.createElement('div');
  thumb.className = 'thumb';

  if (ex.demo && (ex.demo.endsWith('.mp4') || ex.demo.endsWith('.webm'))) {
    const v = document.createElement('video');
    v.src = ex.demo;
    v.controls = true;
    v.muted = true;
    v.playsInline = true;
    v.preload = 'metadata';
    thumb.appendChild(v);
  } else if (ex.gif) {
    const img = document.createElement('img');
    img.src = ex.gif;
    img.alt = ex.name;
    thumb.appendChild(img);
  } else {
    const ph = document.createElement('div');
    ph.textContent = 'Sem m√≠dia';
    ph.style.color = '#64748b';
    thumb.appendChild(ph);
  }

  card.appendChild(info);
  card.appendChild(thumb);
  return card;
}


